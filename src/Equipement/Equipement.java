package Equipement;

public class Equipement {
	
    private String nom;
    private String classe;

    public String getNom()
    {
        return this.nom;
    }

    public void setNom(String nom)
    {
        this.nom = nom;
    }

	public String getCLasse() {
		return classe;
	}

	public void setClasse(String classe) {
		this.classe = classe;
	}
}
